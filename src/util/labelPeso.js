export const createLavelPeso = (cant) => {
	switch (cant) {
		case 'oneandhalfkgPrice':
			return '1.5 Kg';
			break;
		case 'twokgPrice':
			return '2 Kg';
			break;
		case 'threekgPrice':
			return '3 Kg';
			break;
		case 'fourkgPrice':
			return '4 Kg';
			break;
		case 'fivekgPrice':
			return '5 Kg';
			break;
		case 'sixkgPrice':
			return '6 Kg';
			break;
		case 'sevenkgPrice':
			return '7 Kg';
			break;
		case 'eigthkgPrice':
			return '8 Kg';
			break;
		case 'ninekgPrice':
			return '9 Kg';
			break;
		case 'tenkgPrice':
			return '10 Kg';
			break;

		default:
			break;
	}
};
